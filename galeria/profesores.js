
var contenedor = document.getElementById('container-prof');

function paul() {
    var continuar = true;
    while(continuar){
        var nombre = document.getElementById('n-paul');
        var informacion = document.getElementById('i-paul');
        var profe = document.getElementById('paul');
        
        if (nombre.style.display === 'none') {
            nombre.style.display = 'block';
            informacion.style.display = 'none';
            continuar= false;
            window.scrollTo({
                top: 100, 
                behavior: "smooth"
            });
        }else if(informacion.style.display === 'none'){
            informacion.style.display = 'block';
            nombre.style.display = 'none';
            continuar= false;
            window.scrollTo({
                top:100, 
                behavior: "smooth"
            });
            contenedor.insertBefore(profe, contenedor.firstChild);
        }
        else {
            informacion.style.display = 'none';
            window.scrollTo({
                top: 100, 
                behavior: "smooth"
            });
        }
    }
}

